package com.cengel.starbucks.constant;

/**
 * 符号常量
 */
public interface Symbol {
	/**
	 * 点 .
	 */
	String DOT = ".";
	/**
	 * 逗号 ，
	 */
	String COMMA = ",";
	/**
	 * 星号 *
	 */
	String ASTERISK = "*";
	/**
	 * 井号 #
	 */
	String POUND_SIGN = "#";
	/**
	 * 问号?
	 */
	String QUESTION_MARK = "?";
	/**
	 * 双或号||
	 */
	String DOUBLE_OR = "||";
	/**
	 * 转译双或号 \\|\\|
	 */
	String TRANSLATION_DOUBLE_OR = "\\|\\|";
	/**
	 * 与符号 &
	 */
	String AND = "&";
	/**
	 * 等号=
	 */
	String EQUALS = "=";
	/**
	 * 反斜杠 \
	 */
	String BACKSLASH = "\\";
	/**
	 * 双斜杠//
	 */
	String DOUBLE_SLASH = "//";
	/**
	 * 斜杠 /
	 */
	String SLASH = "/";
	/**
	 * 短划线 -
	 */
	String DASHED_LINE = "-";
	/**
	 * 下划线 _
	 */
	String UNDERLINE = "_";
	/**
	 * 百分号 %
	 */
	String PERCENT = "%";
	/**
	 * 多行空格 \\s+
	 */
	String SPACES = "\\s+";
	/**
	 * 空格
	 */
	String SPACE = " ";
	/**
	 * 冒号
	 */
	String COLON = ":";
	/**
	 * 分号
	 */
	String SEMICOLON = ";";
	/**
	 * 加号 +
	 */
	String PLUS = "+";

	/**
	 * Caret
	 */
	String CARET = "^";

	/**
	 * ~波浪，一般用于String分隔，如key~value
	 */
	String WAVE =  "~";



}
